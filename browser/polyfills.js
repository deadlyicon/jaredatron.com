Array.prototype.without = function(i) {
  return this.filter(function(m){ return i !== m; });
};

if (!Array.prototype.sortBy) {
  Array.prototype.sortBy = function(getProp){
    if (typeof getProp !== 'function'){
      const prop = getProp
      getProp = function(member){ return member[prop] }
    }
    return this
      .map(function(member) {
        return [member, getProp(member)]
      })
      .sort(function([,a], [,b]) {
        return a < b ? -1 : b < a ? 1 : 0
      })
      .map(function([member]) {
        return member
      })
  }
};

if (!Object.filter){
  Object.filter = function(object, filterer) {
    return Object.entries(object)
      .reduce(function(newObject, [key, value]) {
        if (filterer(key, value)) newObject[key] = value
          return newObject
      }, {})
  }
}
